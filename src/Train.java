
public class Train {

	public static void main(String[] args) {
		
		
		int[] passengers = {13, 24, 8};
		
		int total = 0;
				
				
		for (int i = 0; i < passengers.length; i++) {
			
			total += passengers[i];
			
			System.out.println(passengers[i]);
			
		}
	
		System.out.println(total);

	}

}
